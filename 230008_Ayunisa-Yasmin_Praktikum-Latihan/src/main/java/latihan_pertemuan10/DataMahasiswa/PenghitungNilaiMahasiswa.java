    public final void tampilkanDataTabel() {
        String[] columnNames = {"NIM", "Nama", "Alamat", "MataKuliah", "Nilai Akhir"};
        Object[][] data = new Object[data_mahasiswa.getAllMahasiswa().size()][3];
        //prosese pendeklarasian
        int i = 0;
        for (Mahasiswa mhs : data_mahasiswa.getAllMahasiswa()) {
        Object arrayMahasiswa[] = {
            mhs.getNIM(),
            mhs.getNama(),
            mhs.getAlamat(),
            mhs.getMataKuliah(),  
            mhs.getNilaiAkhir()      
        };
        
        data[i] = arrayMahasiswa;
        i++;
        }
        
        mahasiswa_data = new DefaultTableModel(data, columnNames);
        mahasiswaData.setModel(mahasiswa_data);
    }
    
