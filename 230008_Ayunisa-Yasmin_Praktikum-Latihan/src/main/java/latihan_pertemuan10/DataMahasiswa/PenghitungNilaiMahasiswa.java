    private void nilaiAkhir(){
        try {
            if (!inputNilai1.getText().equals("") && !inputNilai2.getText().equals("") && !inputNilai3.getText().equals("")
                && !inputNilai4.getText().equals("") && !inputNilai5.getText().equals("")) {
                double nilai1 = Double.parseDouble(inputNilai1.getText());
                double nilai2 = Double.parseDouble(inputNilai2.getText());
                double nilai3 = Double.parseDouble(inputNilai3.getText());
                double nilai4 = Double.parseDouble(inputNilai4.getText());
                double nilai5 = Double.parseDouble(inputNilai5.getText());
                double nilaiAkhir = (nilai1 * 0.10) + (nilai2 * 0.15) + (nilai3 * 0.25) + (nilai4 * 0.15) + (nilai5 * 0.35);
                inputNilaiA.setText(String.valueOf(nilaiAkhir));
            }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Pastikan semua nilainya diisi dengan angka yang valid.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


