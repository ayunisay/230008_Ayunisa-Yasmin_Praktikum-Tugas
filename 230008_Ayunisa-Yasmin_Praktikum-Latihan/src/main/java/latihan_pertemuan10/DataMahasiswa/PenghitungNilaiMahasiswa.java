    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        String NIM =inputNim.getText();
        String Nama = inputNama.getText();
        String Alamat = inputAlamat.getText();
        String MataKuliah = inputMatkul.getText();
        double Nilai1 = Double.parseDouble(inputNilai1.getText());
        double Nilai2 = Double.parseDouble(inputNilai2.getText());
        double Nilai3 = Double.parseDouble(inputNilai3.getText());
        double Nilai4 = Double.parseDouble(inputNilai4.getText());   
        double Nilai5 = Double.parseDouble(inputNilai5.getText());
        
        data_mahasiswa.simpanMahasiswa(NIM, Nama, Alamat, MataKuliah, Nilai1, Nilai2, Nilai3, Nilai4, Nilai5);
        tampilkanDataTabel();
        clearTextFields();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
    }//GEN-LAST:event_simpanActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        int selectedRow = mahasiswaData.getSelectedRow();
        if (selectedRow >= 0) {
            data_mahasiswa.hapusMahasiswa(selectedRow);
            tampilkanDataTabel();
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
        } else {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hapusActionPerformed

